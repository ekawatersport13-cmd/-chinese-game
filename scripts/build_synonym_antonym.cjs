const data = {};

// 添加反义词对
for (const [a, b] of antonymPairs) {
  if (!data[a]) data[a] = { synonyms: [], antonyms: [] };
  if (!data[b]) data[b] = { synonyms: [], antonyms: [] };
  if (!data[a].antonyms.includes(b)) data[a].antonyms.push(b);
  if (!data[b].antonyms.includes(a)) data[b].antonyms.push(a);
}

// 添加同义词
for (const [word, syns] of Object.entries(synonymMap)) {
  if (!data[word]) data[word] = { synonyms: [], antonyms: [] };
  for (const s of syns) {
    if (s !== word && !data[word].synonyms.includes(s)) {
      data[word].synonyms.push(s);
    }
  }
}

// 清理：去重，去掉自引用
const cleaned = {};
for (const [word, rels] of Object.entries(data)) {
  const synonyms = [...new Set(rels.synonyms.filter(s => s !== word))];
  const antonyms = [...new Set(rels.antonyms.filter(a => a !== word))];
  if (synonyms.length > 0 || antonyms.length > 0) {
    cleaned[word] = { synonyms, antonyms };
  }
}

