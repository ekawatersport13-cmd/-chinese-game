for (const [word, syns] of Object.entries(moreSynonymMap)) {
  if (!d[word]) d[word] = { synonyms: [], antonyms: [] };
  for (const s of syns) {
    if (s !== word && !d[word].synonyms.includes(s)) {
      d[word].synonyms.push(s);
    }
    // 反向同义词
    if (!d[s]) d[s] = { synonyms: [], antonyms: [] };
    if (s !== word && !d[s].synonyms.includes(word)) {
      d[s].synonyms.push(word);
    }
  }
}

// 反向引用扩展：对所有已有的同义词和反义词创建反向链接
for (const [word, rels] of Object.entries(d)) {
  for (const syn of rels.synonyms) {
    if (!d[syn]) d[syn] = { synonyms: [], antonyms: [] };
    if (!d[syn].synonyms.includes(word) && word !== syn) d[syn].synonyms.push(word);
  }
  for (const ant of rels.antonyms) {
    if (!d[ant]) d[ant] = { synonyms: [], antonyms: [] };
    if (!d[ant].antonyms.includes(word) && word !== ant) d[ant].antonyms.push(word);
  }
}

// 同义传递：如果 A 和 B 同义，B 和 C 同义，则 A 和 C 也可能是同义
const allWords = Object.keys(d);
const synonymClosure = {};
for (const word of allWords) {
  if (!d[word]) continue;
  const directSyns = d[word].synonyms || [];
  const indirectSyns = new Set(directSyns);
  for (const syn of directSyns) {
    if (d[syn] && d[syn].synonyms) {
      for (const syn2 of d[syn].synonyms) {
        if (syn2 !== word) indirectSyns.add(syn2);
      }
    }
  }
  synonymClosure[word] = [...indirectSyns];
}

